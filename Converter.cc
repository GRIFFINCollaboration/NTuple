Converter::~Converter() {
    if(fOutput->IsOpen()) {
        if(fSettings->WriteTree())
            fTree.Write("tree");
        for(auto list = fHistograms.begin(); list != fHistograms.end(); ++list) {
            fOutput->mkdir(list->first.c_str());
            fOutput->cd(list->first.c_str());
            list->second->Write();
        }
        fOutput->Close();
    }
}

